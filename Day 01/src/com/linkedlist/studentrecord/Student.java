package com.linkedlist;

public class Student {

        String name;
        int age;
        int rollNumber;
        char grade;
        Student next;

        public Student(String name, int age, int rollNumber, char grade)
        {
            this.name=name;
            this.age=age;
            this.rollNumber=rollNumber;
            this.grade=grade;
            this.next=null;
        }
    }


